package com.dimata.demo.app.prochain_app.services.dbHandler;

import com.dimata.demo.app.prochain_app.core.api.DbHandlerBase;
import com.dimata.demo.app.prochain_app.models.table.ContactList;
import com.dimata.demo.app.prochain_app.services.repo.ContactListRepo;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.data.r2dbc.repository.R2dbcRepository;
import org.springframework.stereotype.Component;

import lombok.EqualsAndHashCode;
import reactor.core.publisher.Flux;
import reactor.core.publisher.Mono;

@Component
@EqualsAndHashCode(callSuper = true)
public class ContactListDbHandler extends DbHandlerBase <ContactList, Long>{
    
    @Autowired
    private ContactListRepo repo;

    @Override
    protected R2dbcRepository<ContactList, Long> getRepository() {
        return repo;
    }

    @Override
    protected Mono<ContactList> setGenerateId(ContactList record) {
        return Mono.just(record)
            .map(z -> {
                long id = getGenerateUtil().generateOID();
                z.setInsertId(id);
                return z;
            });
    }

    @Override
    protected Flux<ContactList> setGenerateIdBatch(Flux<ContactList> records) {
        return records
            .map(rec -> {
                long id = getGenerateUtil().generateOID();
                rec.setInsertId(id);
                return rec;
            });
    }
}
